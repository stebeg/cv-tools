package io.github.stebeg.comicvine.character;

/**
 * Represents the available character attributes.
 */
public class CharacterAttribute {

  /**
   * The attribute name for the id of the character.
   */
  public static final String ID = "id";
  /**
   * The attribute name for the name of the character.
   */
  public static final String NAME = "name";

  /**
   * The attribute name for the real name of the character.
   */
  public static final String REAL_NAME = "real_name";
  /**
   * The attribute name for the summary of the character.
   */
  public static final String SUMMARY = "deck";
  /**
   * The attribute name for the description of the character.
   */
  public static final String DESCRIPTION = "description";
  /**
   * The attribute name for the aliases of the character.
   */
  public static final String ALIASES = "aliases";

  /**
   * The attribute name for the reference of the publisher of the character.
   */
  public static final String PUBLISHER = "publisher";
  /**
   * The attribute name for the images of the character.
   */
  public static final String IMAGE = "image";

  /**
   * The attribute name for the gender of the character.
   */
  public static final String GENDER = "gender";
  /**
   * The attribute name for the origin of the character.
   */
  public static final String ORIGIN = "origin";
  /**
   * The attribute name for the birthdate of the character.
   */
  public static final String BIRTH = "birth";

  /**
   * The attribute name for the references of the creators of the character.
   */
  public static final String CREATORS = "creators";

  /**
   * The attribute name for the ID of the origin.
   */
  public static final String ORIGIN_ID = "id";
  /**
   * The attribute name for the name of the origin.
   */
  public static final String ORIGIN_NAME = "name";

  private CharacterAttribute() {
  }
}
