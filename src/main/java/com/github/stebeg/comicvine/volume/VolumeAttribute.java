package com.github.stebeg.comicvine.volume;

/**
 * Contains the names of the attributes of a volume from the Comicvine API.
 */
public class VolumeAttribute {

  /**
   * The name of the attribute for the volume's unique ID.
   */
  public static final String ID = "id";
  /**
   * The name of the attribute for the volume's name.
   */
  public static final String NAME = "name";

  /**
   * The attribute name for the summary of the volume.
   */
  public static final String SUMMARY = "deck";
  /**
   * The attribute name for the description of the volume.
   */
  public static final String DESCRIPTION = "description";
  /**
   * The attribute name for the aliases of the volume.
   */
  public static final String ALIASES = "aliases";

  /**
   * The attribute name for the reference of the publisher of the volume.
   */
  public static final String PUBLISHER = "publisher";
  /**
   * The attribute name for the images of the volume.
   */
  public static final String IMAGE = "image";

  /**
   * The attribute name for the number of issues in the volume.
   */
  public static final String ISSUE_COUNT = "count_of_issues";

  /**
   * The attribute name for the start year of the volume.
   */
  public static final String START_YEAR = "start_year";
  /**
   * The attribute name for the reference of the first issue of the volume.
   */
  public static final String FIRST_ISSUE = "first_issue";
  /**
   * The attribute name for the reference of the last issue of the volume.
   */
  public static final String LAST_ISSUE = "last_issue";

  /**
   * The attribute name for the list of issues of the volume.
   */
  public static final String ISSUES = "issues";

}
