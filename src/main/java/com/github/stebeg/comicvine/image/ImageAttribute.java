package com.github.stebeg.comicvine.image;

/**
 * Contains the names of the attributes of an image from the Comicvine API.
 */
public class ImageAttribute {

  /**
   * The name of the attribute for the icon url.
   */
  public static final String ICON_URL = "icon_url";
  /**
   * The name of the attribute for the medium url.
   */
  public static final String MEDUIM_URL = "medium_url";
  /**
   * The name of the attribute for the screen url.
   */
  public static final String SCREEN_URL = "screen_url";
  /**
   * The name of the attribute for the screen large url.
   */
  public static final String SCREEN_LARGE_URL = "screen_large_url";
  /**
   * The name of the attribute for the small url.
   */
  public static final String SMALL_URL = "small_url";
  /**
   * The name of the attribute for the super url.
   */
  public static final String SUPER_URL = "super_url";
  /**
   * The name of the attribute for the thumb url.
   */
  public static final String THUMB_URL = "thumb_url";
  /**
   * The name of the attribute for the tiny url.
   */
  public static final String TINY_URL = "tiny_url";
  /**
   * The name of the attribute for the original url.
   */
  public static final String ORIGINAL_URL = "original_url";
  /**
   * The name of the attribute for the image tags.
   */
  public static final String IMAGE_TAGS = "image_tags";

  private ImageAttribute() {
  }
}
