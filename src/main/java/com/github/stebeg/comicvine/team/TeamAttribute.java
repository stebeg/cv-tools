package com.github.stebeg.comicvine.team;

/**
 * Contains the names of the attributes of a team from the Comicvine API.
 */
public class TeamAttribute {

  /**
   * The name of the attribute for the team's unique ID.
   */
  public static final String ID = "id";
  /**
   * The name of the attribute for the team's name.
   */
  public static final String NAME = "name";

  /**
   * The name of the attribute for the team's aliases.
   */
  public static final String ALIASES = "aliases";
  /**
   * The name of the attribute for the team's brief summary.
   */
  public static final String SUMMARY = "deck";
  /**
   * The name of the attribute for the team's description.
   */
  public static final String DESCRIPTION = "description";

  /**
   * The attribute name for the reference of the publisher of the team.
   */
  public static final String PUBLISHER = "publisher";
  /**
   * The attribute name for the images of the team.
   */
  public static final String IMAGE = "image";

  /**
   * The attribute name for the members of the team.
   */
  public static final String MEMBERS = "characters";

}
